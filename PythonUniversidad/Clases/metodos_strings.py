# Introducción
# Existen varios métodos útiles para manipular y trabajar con
# cadenas o frases

# .capitalize ()
# Convierte el primer carácter de la cadena a mayúsculas

# .lower()
# Convierte todos los caracteres a minúsculas

# .upper()
# Convierte todos los caracteres a mayúsculas

# .title()
# Convierte el primer carácter de cada palabra a mayúsculas

# .strip()
# Elimina los espacios en blanco al principio y al final de la
# cadena

# .replace(old, new)
# Reemplaza una subcadena por otra

# .split(sep)
# Divide la cadena en una lista, usando un separador
# especificado, si no se especifica se divide por espacios en blanco

# .len()
# Devuelve la longitud de la cadena